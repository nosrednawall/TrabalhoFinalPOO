	package Exercicio2;
	
	public class Aluno {
		
		int ag;
		String nome;
		int idade;
		String cpf;
		
		public int getAg() {
			return ag;
		}
		public void setAg(int ag) {
			this.ag = ag;
		}
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public int getIdade() {
			return idade;
		}
		public void setIdade(int idade) {
			this.idade = idade;
		}
		public String getCpf() {
			return cpf;
		}
		public void setCpf(String cpf) {
			this.cpf = cpf;
		}
	
	}
